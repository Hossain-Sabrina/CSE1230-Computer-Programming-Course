#include<iostream>
using namespace std;
int main()
{
    long long int a,b,A, B, C;
    cin>>a>>b;
    A=(a+b);
    B=(a*b);
    C=(a-b);
    cout<<""<< a << " + " << b << " = " << A <<endl;
    cout<<""<< a << " * " << b << " = " << B <<endl;
    cout<<""<< a << " - " << b << " = " << C <<endl;
    return 0;
}
